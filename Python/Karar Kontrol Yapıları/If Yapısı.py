"""
-If yapısı "sadece true" değer dönen değerin işlemini yapar.
"""

samsung=3000
xiaomi=5300

if samsung > xiaomi:  #False ifade çıkar. False ifade işlem yapmaz.
    print("Samsung, Xiaomi'den daha pahalıdır.")

if samsung < xiaomi: #True ifade çıkar. True ifade işlem yapar.
    print("Samsung, Xiaomi'den daha ucuzdur.")

if samsung == xiaomi: #False ifade çıkar. False ifade işlem yapmaz.
    print("Samsung'un fiyatı Xiaomi'nin fiyatına eşittir.")

if samsung != xiaomi: #True ifade çıkar. True ifade işlem yapar.
    print("Samsung ile Xiaomi'nin fiyatları farklıdır.")